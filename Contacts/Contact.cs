public class Contact{

    //We declare a new class called Contact that will accept 4 variables. 
    // Each Contact will have a name, order, email, and number associated with it.
    public string name;
    public int order;
    public string email;
    public string number;

    public Contact(string name, int order, string email, string number){
        this.name = name;
        this.order = order;
        this.email = email;
        this.number = number;

    }
    

}
